import SwiftUI

public extension Font {
    static func alertRegular() -> Font {
        Font(UIFont.alertRegular())
    }

    static func headerExtraLarge() -> Font {
        Font(UIFont.headerExtraLarge())
    }

    static func headerLarge() -> Font {
        Font(UIFont.headerLarge())
    }

    static func headerMedium() -> Font {
        Font(UIFont.headerMedium())
    }

    static func headerRegular() -> Font {
        Font(UIFont.headerRegular())
    }

    static func headerSmall() -> Font {
        Font(UIFont.headerSmall())
    }

    static func headerExtraSmall() -> Font {
        Font(UIFont.headerExtraSmall())
    }

    static func bodyExtraLarge() -> Font {
        Font(UIFont.bodyExtraLarge())
    }

    static func bodyLarge() -> Font {
        Font(UIFont.bodyLarge())
    }

    static func bodyRegular() -> Font {
        Font(UIFont.bodyRegular())
    }

    static func bodySmall() -> Font {
        Font(UIFont.bodySmall())
    }

    static func bodyXSmall() -> Font {
        Font(UIFont.bodyXSmall())
    }

    static func sfProRegular(size: Double) -> Font {
        Font(UIFont.sfProRegular(size: size))
    }
}
