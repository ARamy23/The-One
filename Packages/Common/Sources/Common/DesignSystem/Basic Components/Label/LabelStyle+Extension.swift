import UIKit

public extension LabelStyle {
    static func headerExtraLarge() -> LabelStyle {
        LabelStyle(
            font: UIFont.headerExtraLarge())
    }

    static func headerLarge() -> LabelStyle {
        LabelStyle(
            font: UIFont.headerLarge())
    }

    static func headerMedium() -> LabelStyle {
        LabelStyle(
            font: UIFont.headerMedium())
    }

    static func headerRegular() -> LabelStyle {
        LabelStyle(
            font: UIFont.headerRegular())
    }

    static func headerSmall() -> LabelStyle {
        LabelStyle(
            font: UIFont.headerSmall())
    }

    static func headerExtraSmall() -> LabelStyle {
        LabelStyle(
            font: UIFont.headerExtraSmall())
    }

    static func bodyExtraLarge() -> LabelStyle {
        LabelStyle(
            font: UIFont.bodyExtraLarge())
    }

    static func bodyLarge() -> LabelStyle {
        LabelStyle(
            font: UIFont.bodyLarge())
    }

    static func bodyRegular() -> LabelStyle {
        LabelStyle(
            font: UIFont.bodyRegular())
    }

    static func bodySmall() -> LabelStyle {
        LabelStyle(
            font: UIFont.bodySmall())
    }

    static func bodyXSmall() -> LabelStyle {
        LabelStyle(
            font: UIFont.bodyXSmall())
    }
}
